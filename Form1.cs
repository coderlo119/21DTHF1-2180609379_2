        private void btnC_Click(object sender, EventArgs e)
        {
            foreach (Button a in DanhSachChon)
            {
                int b = int.Parse(a.Text);
                if (b <= 5)
                {
                    a.BackColor = Color.Yellow;
                    intThanhTien += 30000;
                }
                if (5 < b && b <= 10)
                {
                    a.BackColor = Color.Yellow;
                    intThanhTien += 400000;
                }
                if (10 < b && b <= 15)
                {
                    a.BackColor = Color.Yellow;
                    intThanhTien += 500000;
                }
                if (15 < b && b <= 20)
                {
                    a.BackColor = Color.Yellow;
                    intThanhTien += 80000;
                }
            }
            textBox1.Text = intThanhTien.ToString();
            intThanhTien = 0;
            DanhSachChon = new List<Button>();
        }

        private void btnH_Click(object sender, EventArgs e)
